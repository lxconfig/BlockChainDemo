

def main():
    """
        合并两个字典
    """
    x = {"张三": 33, "李四": 32}
    y = {"王五": 53}

    # 重复的会覆盖掉之前的值
    z = {**x, **y, "赵六": 43, "王五": 78}
    print(z)

    # 或者用update函数
    y.update(x)
    print(y)

    print("将两个列表合并成字典:")
    a = ["x", "y", "c"]
    b = [35, 44]

    # 即使两个列表长度不等，也不会报错，只是不显示多出来的值
    # zip()函数将可迭代对象合并成元组
    c = dict(zip(a, b))
    print(c)

if __name__ == "__main__":
    main()